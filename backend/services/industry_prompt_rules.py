def get_industry_rules(category: str) -> dict:
    """업종 코드로 AI 플랫폼별 최적화 규칙 반환. 미등록 업종은 'other' fallback."""
    key = normalize_category(category)
    return _RULES.get(key, _RULES["other"])


def build_industry_system_prompt(category: str) -> str:
    """업종 코드를 받아 업종별 AI 최적화 규칙이 추가된 시스템 프롬프트 반환"""
    rules = get_industry_rules(category)

    naver_lines = "\n".join(f"  - {s}" for s in rules["naver"])
    chatgpt_lines = "\n".join(f"  - {s}" for s in rules["chatgpt"])
    google_lines = "\n".join(f"  - {s}" for s in rules["google"])

    industry_section = f"""

## 이 업종의 AI 플랫폼별 핵심 신호 (이 규칙 기반으로 우선순위 결정)
### 네이버 AI 브리핑이 이 업종에서 보는 것:
{naver_lines}
### ChatGPT·글로벌 AI가 이 업종에서 보는 것:
{chatgpt_lines}
### Google AI가 이 업종에서 보는 것:
{google_lines}
### Cross-AI 진단 기준:
  - {rules["cross_ai_note"]}"""

    return _BASE_SYSTEM_PROMPT + industry_section
